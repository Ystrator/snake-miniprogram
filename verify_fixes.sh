grep -q "min-width: 88rpx" app.wxss && echo "  ✅ 全局热区样式已定义(88rpx)" || echo "  ❌ 全局热区样式未定义"
grep -c "large-tap" pages/index/index.wxml > /tmp/count.txt && echo "  ✅ 首页应用large-tap类: $(cat /tmp/count.txt)个元素"
